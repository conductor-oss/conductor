package com.netflix.conductor.postgreslock.config;

import org.springframework.boot.context.properties.ConfigurationProperties;

@ConfigurationProperties("conductor.postgres-lock")
public class PostgresLockProperties {

    /** URL for connecting to PostgreSQL database */
    private String url;

    /** Username for connecting to PostgreSQL database */
    private String username;

    /** Password for connecting to PostgreSQL database */
    private String password;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
