package org.conductoross.conductor.common.integrations.zoho;

public class ZohoBooksConnectionResponse {

    private String connectionId;
    private String organizationId;
    private Long createdAt;
    private Long updatedAt;

    public ZohoBooksConnectionResponse() {}

    public ZohoBooksConnectionResponse(ZohoBooksConnection connection) {
        this.connectionId = connection.getConnectionId();
        this.organizationId = connection.getOrganizationId();
        this.createdAt = connection.getCreatedAt();
        this.updatedAt = connection.getUpdatedAt();
    }

    public String getConnectionId() {
        return connectionId;
    }

    public void setConnectionId(String connectionId) {
        this.connectionId = connectionId;
    }

    public String getOrganizationId() {
        return organizationId;
    }

    public void setOrganizationId(String organizationId) {
        this.organizationId = organizationId;
    }

    public Long getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Long createdAt) {
        this.createdAt = createdAt;
    }

    public Long getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(Long updatedAt) {
        this.updatedAt = updatedAt;
    }
}
